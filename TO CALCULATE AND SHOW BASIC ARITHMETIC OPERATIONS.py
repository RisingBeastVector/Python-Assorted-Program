#PYTHON PROGRAM TO CALCULATE AND SHOW BASIC ARITMETIC OPERATIONS LIKE
# +,-,*,%,**(POWER) AFTER ACCEPTING TWO NUMBERS.
a = int(input('ENTER THE VALUE OF "a" : '))
b = int(input('ENTER THE VALUE OF "b" : '))
add = a+b
sub = a-b
mult = a*b
div = a//b
rem = a%b
pow = a**b
print()
print("============================='OUTPUT'===============================")
print()
print('AFTER ADDITION : ', add)
print('AFTER SUBTRACTION: ', sub)
print('AFTER MULTIPLICATION : ', mult)
print('AFTER DIVISION, THE QUOTIENT IS : ', div)
print('AFTER DIVISION, THE REMAINDER IS : ', rem)
print('AFTER POWERING BOTH THE VALUES: ', pow)
